package ee.bcs.practice.exercise;

public class IncrementAndDecrementOperatorsExercise {

    public static void main(String[] args) {

        // Exercise 1: Implement Pre-Increment Operation
        int a = 5;
        ++a;
        int resultPreIncrement = a;
        System.out.println("Result of Pre-Increment: " + resultPreIncrement);

        // Exercise 2: Implement Post-Increment Operation
        int b = 5;
        int resultPostIncrement = b++;
        System.out.println("Result of Post-Increment: " + resultPostIncrement);

        // Exercise 3: Implement Pre-Decrement Operation
        int c = 5;
        --c;
        int resultPreDecrement = c;
        System.out.println("Result of Post-Increment: " + resultPreDecrement);

        // Exercise 4: Implement Post-Decrement Operation
        int d = 5;
        int resultPostDecrement = d--;
        System.out.println("Result of Post-Increment: " + resultPostDecrement);

        // Run main method

    }
}
